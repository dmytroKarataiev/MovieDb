package com.adkdevelopment.moviesdb.data.model;

import com.google.gson.annotations.Expose;
import com.google.gson.annotations.SerializedName;

import java.util.ArrayList;
import java.util.List;

/**
 * Created by karataev on 5/26/16.
 */
public class ActorImage {

    @SerializedName("id")
    @Expose
    private long id;
    @SerializedName("profiles")
    @Expose
    private List<ActorPhotos> profiles = new ArrayList<>();

    /**
     *
     * @return
     * The id
     */
    public long getId() {
        return id;
    }

    /**
     *
     * @param id
     * The id
     */
    public void setId(long id) {
        this.id = id;
    }

    /**
     *
     * @return
     * The profiles
     */
    public List<ActorPhotos> getProfiles() {
        return profiles;
    }

    /**
     *
     * @param profiles
     * The profiles
     */
    public void setProfiles(List<ActorPhotos> profiles) {
        this.profiles = profiles;
    }

}
